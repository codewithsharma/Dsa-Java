public class WhileLoop {
    public static void main(String[] args) {
        int i = 0; // Initialization of the loop control variable.
        while (i < 5) { // The condition that controls whether or not to
            // execute the code block is "i<5". If it evaluates to true,
            System.out.println(i);
            i++; // Incrementing the value of the loop control variable by 1.
        }
    }
}
